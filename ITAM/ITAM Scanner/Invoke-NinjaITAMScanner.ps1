$lbDevices.Add_SelectionChanged({
    $btnRemoveDevice.IsEnabled = ($lbDevices.SelectedIndex -ge 0)
})

$btnRemoveDevice.Add_Click({
    $idx = $lbDevices.SelectedIndex
    if ($idx -ge 0 -and $idx -lt $script:Devices.Count) {
        $script:Devices.RemoveAt($idx)
        $lbDevices.Items.RemoveAt($idx)
        Update-ScanState
    }
    $tbScannerInput.Focus()
})

$btnAssign.Add_Click({
    if ($null -eq $script:UserInfo -or $script:Devices.Count -eq 0) { return }

    $ownerUid = $script:UserInfo.Uid
    $total    = $script:Devices.Count
    $success  = 0
    $failed   = 0
    $errors   = [System.Collections.Generic.List[string]]::new()

    $btnAssign.IsEnabled = $false
    $btnReset.IsEnabled  = $false

    for ($i = 0; $i -lt $total; $i++) {
        $dev = $script:Devices[$i]
        $lblStatus.Text = "Assigning $($dev.Name) ($($i + 1)/$total)..."
        Push-UIUpdate

        try {
            Set-NinjaDeviceOwner -DeviceId $dev.Id -OwnerUid $ownerUid
            $success++
        } catch {
            $failed++
            $errors.Add("$($dev.Name) (ID $($dev.Id)): $($_.Exception.Message)")
        }
    }

    $summary = "Done. $success of $total device(s) assigned to $($script:UserInfo.Name)."
    if ($failed -gt 0) {
        $summary += " $failed failed."
        $detail = $summary + "`r`n`r`nErrors:`r`n" + ($errors -join "`r`n")
        [System.Windows.MessageBox]::Show($detail, 'Assignment Results', 'OK', 'Warning') | Out-Null
    } else {
        [System.Media.SystemSounds]::Asterisk.Play()
    }

    $lblStatus.Text = $summary
    $btnAssign.IsEnabled = $true
    $btnReset.IsEnabled  = $true
    $tbScannerInput.Focus()
})

$btnReset.Add_Click({
    Reset-All
})
