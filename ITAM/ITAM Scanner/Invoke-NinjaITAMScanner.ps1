#Requires -Version 5.1
<#
.SYNOPSIS
  WPF-based ITAM QR scanner for assigning NinjaOne devices to users.

.DESCRIPTION
  Standalone PowerShell WPF application that works with a USB barcode scanner
  (e.g. Eyoyo EYH2) to scan NinjaOne QR codes. The workflow is:

    1. Sign in to NinjaOne using OAuth Authorization Code + PKCE.
    2. Scan a user QR code  (URL: /#/userDashboard/{id}/overview).
    3. Scan one or more device QR codes (URL: /#/deviceDashboard/{id}/overview).
    4. Click "Assign" to set the user as the owner of all scanned devices.

  QR codes are generated by New-NinjaDeviceQRCode.ps1 and New-NinjaUserQRCode.ps1.

  The NinjaOne OAuth application must be configured as a Native (Authorization
  Code) type with redirect URI http://localhost (port-wildcard for native apps
  per RFC 8252).

.PARAMETER NinjaOneInstance
  NinjaOne instance hostname or base URL.
  Default: env:NINJA_BASE_URL or ca.ninjarmm.com.

.PARAMETER ClientId
  OAuth application Client ID.
  Default: env:NinjaOneClientId.

.EXAMPLE
  .\Invoke-NinjaITAMScanner.ps1

.EXAMPLE
  .\Invoke-NinjaITAMScanner.ps1 -NinjaOneInstance app.ninjarmm.com -ClientId "your-client-id"
#>

