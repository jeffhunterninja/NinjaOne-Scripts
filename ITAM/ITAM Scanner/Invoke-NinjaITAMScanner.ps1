#region UI Helpers
function Update-ScanState {
    if ($null -eq $script:UserInfo) {
        $lblStatus.Text = 'Scan a user QR code (Step 1).'
        $btnAssign.IsEnabled = $false
    } elseif ($script:Devices.Count -eq 0) {
        $lblStatus.Text = "User: $($script:UserInfo.Name). Scan device QR codes (Step 2)."
        $btnAssign.IsEnabled = $false
    } else {
        $lblStatus.Text = "Ready to assign $($script:Devices.Count) device(s) to $($script:UserInfo.Name)."
        $btnAssign.IsEnabled = $true
    }
    $lblDeviceCount.Text = "$($script:Devices.Count) device(s) scanned"
    $btnRemoveDevice.IsEnabled = ($lbDevices.SelectedIndex -ge 0)
}

function Reset-All {
    $script:UserInfo = $null
    $script:Devices.Clear()
    $lbDevices.Items.Clear()
    $lblUserInfo.Text = 'No user scanned. Scan a user QR code to begin.'
    $lblUserInfo.Foreground = [System.Windows.Media.Brushes]::Gray
    $btnClearUser.Visibility = 'Collapsed'
    Update-ScanState
    $tbScannerInput.Clear()
    $tbScannerInput.Focus()
}

function Push-UIUpdate {
    $frame = [System.Windows.Threading.DispatcherFrame]::new()
    [System.Windows.Threading.Dispatcher]::CurrentDispatcher.BeginInvoke(
        [System.Windows.Threading.DispatcherPriority]::Background,
        [Action]{ $frame.Continue = $false }
    )
    [System.Windows.Threading.Dispatcher]::PushFrame($frame)
}
